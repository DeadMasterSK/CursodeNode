const mostrarMenu = () =>{
  return new Promise(resolve =>{
    console.clear();
  
    
  
    const readLine = require('readline').createInterface({
      input: process.stdin,
      output: process.stdout
    });
  
    readLine.question('Seleccione una opcion: '.red, (opt) =>{
      readLine.close();
      resolve(opt);
    });
  })
}

